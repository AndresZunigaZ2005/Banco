package uniquindio.programacion1.parqueadero.model;

public class Posicion {
private int i, j;

public Posicion(int i, int j) {
	super();
	this.i = i;
	this.j = j;
}

public int getI() {
	return i;
}

public void setI(int i) {
	this.i = i;
}

public int getJ() {
	return j;
}

public void setJ(int j) {
	this.j = j;
}

}
